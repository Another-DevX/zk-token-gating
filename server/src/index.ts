server.addMethod(
  'resolve_foreign_call',
  async (params: any): Promise<ForeignCallResult> => {
    if (params[0].function !== 'getNftOwning') {
      throw Error('Unexpected foreign call');
    }

    const ethAddress = params[0].inputs[0][0];
    const formattedAddress = ethAddress.startsWith('0x')
      ? ethAddress
      : `0x${ethAddress.slice(-40)}`; 

    const publicClient = createPublicClient({
      transport: http('http://127.0.0.1:8545'),
    });

    try {
      const data = (await publicClient.readContract({
        address: NFT_ADDRESS as `0x${string}`,
        abi: NFT_ABI,
        functionName: 'balanceOf',
        args: [formattedAddress],
      })) as `0x${string}`;
      return { values: [[data.toString()]] };
    } catch (error) {
      console.error('Error al verificar el propietario del NFT:', error);
      return { values: [['0x0']] };
    }
  }
);

app.post('/', (req, res) => {
  const jsonRPCRequest = req.body;
  server.receive(jsonRPCRequest).then((jsonRPCResponse) => {
    if (jsonRPCResponse) {
      res.json(jsonRPCResponse);
    } else {
      res.sendStatus(204);
    }
  });
});

